package com.lyswzs.bean;

public class StudentScore {
	private String cour_name;
	private String teac_name;
	private String score_score;
	public String getCour_name() {
		return cour_name;
	}
	public void setCour_name(String cour_name) {
		this.cour_name = cour_name;
	}
	public String getTeac_name() {
		return teac_name;
	}
	public void setTeac_name(String teac_name) {
		this.teac_name = teac_name;
	}
	public String getScore_score() {
		return score_score;
	}
	public void setScore_score(String score_score) {
		this.score_score = score_score;
	}
	
}
